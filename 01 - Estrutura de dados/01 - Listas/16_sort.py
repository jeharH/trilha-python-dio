linguagens = ["python", "js", "c", "java", "csharp"]
linguagens.sort()  # ["c", "csharp", "java", "js", "python"]
print(linguagens) # ordenação em ordem alfabética (padrao)

linguagens = ["python", "js", "c", "java", "csharp"]
linguagens.sort(reverse=True)  # ["python", "js", "java", "csharp", "c"]
print(linguagens) # ordenação em ordem alfabética reversa (espelhamento)

linguagens = ["python", "js", "c", "java", "csharp"]
linguagens.sort(key=lambda x: len(x))  # ["c", "js", "java", "python", "csharp"]
print(linguagens) # lê com LEN o tamanho de cada objeto da lista e ordena em ordem crescente

linguagens = ["python", "js", "c", "java", "csharp"]
linguagens.sort(key=lambda x: len(x), reverse=True)  # ["python", "csharp", "java", "js", "c"]
print(linguagens) # le com o LEN o tamanho de cada objeto da lista e ordena em ordem decresecnte por causa do espelhamento do reverse


