frutas = ["laranja", "maca", "uva"] # coloca strings dentro de uma lista
print(frutas)

frutas = [] # lista vazia
print(frutas)

letras = list("python") # itera a string python, letra por letra e coloca dentro da lista letras
print(letras)

numeros = list(range(10)) # itera a numeração de 0 a 10 e coloca na lista numeros
print(numeros)

carro = ["Ferrari", "F8", 4200000, 2020, 2900, "São Paulo", True] # valores de todos os tipos dentro de uma lista
